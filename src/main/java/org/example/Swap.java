package org.example;

public class Swap {
    public int i;
    public int j;
    public int sortedElement;

    String message;

    public Swap(int i, int j, int sortedElement, String message) {
        this.i = i;
        this.j = j;
        this.sortedElement = sortedElement;
        this.message = message;
    }
}
